using System;
//Операторы отношений
//Операции сравнения
// == равно; != не равно; >< больше, меньше; >=,<= больше, меньше или равно


namespace _12_OperatoriOtnoshSravn
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 5;

            int b = 4;

            Console.WriteLine(a == b);

            Console.WriteLine(a != b);

            Console.WriteLine(a > b);

            Console.WriteLine(a < b);

            Console.WriteLine(a >= b);

            Console.WriteLine(a <= b);



        }
    }
}
